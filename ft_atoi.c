int	ft_atoi(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	if (str == "-2147483648")
		return (-2147483648);
	if (str[0] == '-' || str[0] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - 48);
		i++;
	}
	if (str[0] == '-')
		n = -n;
	return (n);
}