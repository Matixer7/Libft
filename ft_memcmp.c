int ft_memcmp(void *s1, void *s2, unsigned int n)
{
    unsigned char *p1;
    unsigned char *p2;

    p1 = s1;
    p2 = s2;
    while (n--)
    {
        if (*p1 != *p2)
            return (*p1-*p2);
        p1++;
        p2++;
    }
    return (0);
}
